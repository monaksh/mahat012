public class java3A {
    public static void main(String[] args) {
        int num1 = 2, num2 = 4;

        // Logical operations
        System.out.println((num1>num2) || (num1<num2)); 
        System.out.println((num1>num2) && (num1<num2));   
        System.out.println(!(num1>num2)); 

        // Relational comparisons
        System.out.println(num1>=num2);
        System.out.println(num1<=num2);
        System.out.println(num1>num2);
        System.out.println(num1<num2); 
        System.out.println(num1!=num2); 
        System.out.println(num1==num2); 
 
    }
}
